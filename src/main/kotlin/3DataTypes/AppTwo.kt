package `3DataTypes`

fun main(){
    // Byte
    val myNum: Byte = 100
    println(myNum)

    // Short
    val myNumTwo: Short = 5000
    println(myNumTwo)

    // Int
    val myNumThree: Int = 100000
    println(myNumThree)

    // Long
    val myNumFour: Long = 15000000000L
    println(myNumFour)

    // Int vs Long
    val myNum1: Int = 2147483647  // Int
    val myNum2: Long = 2147483648L  // Long
    println(myNum1)
    println(myNum2)
}