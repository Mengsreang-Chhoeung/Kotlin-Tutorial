package `3DataTypes`

fun main(){
    /* --------------------------------------
     *  val myNum = 5             // Int
     *  val myDoubleNum = 5.99    // Double
     *  val myLetter = 'D'        // Char
     *  val myBoolean = true      // Boolean
     *  val myText = "Hello"      // String
     * ------------------------------------- */

    val myNum: Int = 5                // Int
    val myDoubleNum: Double = 5.99    // Double
    val myLetter: Char = 'D'          // Char
    val myBoolean: Boolean = true     // Boolean
    val myText: String = "Hello"      // String

    println(myNum)
    println(myDoubleNum)
    println(myLetter)
    println(myBoolean)
    println(myText)
}