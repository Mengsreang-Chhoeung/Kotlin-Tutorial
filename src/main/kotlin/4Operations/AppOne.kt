package `4Operations`

fun main(){
    // Addition
    val x: Int = 100 + 50
    println(x)

    // Subtraction
    val y: Int = 100 - 50
    println(y)

    // Multiplication
    val a: Int = 100 * 50
    println(a)

    // Division
    val b: Int = 100 / 50
    println(b)

    // Modulus
    val c: Int = 100 % 30
    println(c)

    // Increment
    var d: Int = 100
    d++
    ++d
    println(d)

    // Decrement
    var e: Int = 200
    e--
    --e
    println(e)


}