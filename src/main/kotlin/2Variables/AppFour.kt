fun main(){
    val pi: Double = 3.14159265359
    println(pi)

    val name: String = "John"
    println("Hello " + name)

    val firstName: String = "John "
    val lastName: String = "Doe"
    val fullName: String = firstName + lastName
    println(fullName)

    val x: Int = 5
    val y: Int = 6
    println(x + y) // Print the value of x + y
}